package numbersv2;

public class NumbersV2
{
    public static void main(String[] args)
    {
        // part 1
        long one = 2000000000;
        long two = 2000000000;
        System.out.println(one + two);
        System.out.println("");

        //part 2
        long one2 = 10000;
        long two2 = 10000;
        long result = one2 + two2;
        System.out.println(result);
        System.out.println("");

        //part 3
        int first = 8;
        int second = 5;

        System.out.println(first + second);
        System.out.println(second - first);
        System.out.println(first * second);
        System.out.println(first / second);
        System.out.println(first % second);
        System.out.println("");

        //part 4



    }
}
